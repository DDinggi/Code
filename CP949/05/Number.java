class Number {
    int add(int a, int b) {
        System.out.println("int add(int a, int b)");
        return a + b;
    }
    float add(float a, float b) {
        System.out.println("float add(float a, float b)");
        return a + b;
    }
    int add(short a, int b) {
        System.out.println("int add(short a, int b)");
        return a + b;    
    }
}
